#   연습문제 3

# 문제: 1부터 n까지 연속한 숫자의 제곱의 합을 구하는 공식은 (n(n+1)(2n+1))//6 로 알려져 있습니다.
#       for 반복문 대신 이 공식을 이용하면 알고리즘의 계산 복잡도는 O(1)과 O(n) 중 무엇이 될까요?

# 입력:
# 출력:

def sum_sq2(n):
    return (n*(n+1)*(2*n+1)) // 6

print(sum_sq2(10))
print(sum_sq2(100))

# O(1)



# 문제: 1부터 n까지 연속한 숫자의 제곱의 합을 구하는 프로그램을 for 반복문으로 만들어보세요.
#       (예를 들어 n = 10이라면 1^2+2^2+3^2+...+10^2 = 385를 계산하는 프로그램입니다.)
# 입력:
# 출력:

def sum_sq(n):
    sum = 0 
    for i in range(1, n+1):
        b = i * i
        sum = sum + b
    return sum

print(sum_sq(10))
print(sum_sq(100))

